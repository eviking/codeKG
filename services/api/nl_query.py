def _extract_cypher(text: str) -> str:
    # 1. Prefer explicit ```cypher ... ``` or ``` ... ``` block
    match = re.search(r"```(?:cypher)?\s*(.*?)```", text, re.DOTALL | re.IGNORECASE)
    if match:
        return match.group(1).strip()

    # 2. Scan line-by-line for the first line that looks like Cypher
    #    (starts with a Cypher keyword, case-insensitive)
    cypher_keywords = re.compile(
        r"^\s*(MATCH|OPTIONAL\s+MATCH|WITH|UNWIND|RETURN|CREATE|MERGE|WHERE|CALL)\b",
        re.IGNORECASE,
    )
    lines = text.splitlines()
    start = None
    for i, line in enumerate(lines):
        if cypher_keywords.match(line):
            start = i
            break

    if start is not None:
        return "\n".join(lines[start:]).strip()

    # 3. Last resort — return stripped text (may still fail, but gives a clear error)
    return text.strip().strip("`")


def _run_cypher(driver: Driver, cypher: str, **params) -> tuple[list[dict], str | None]:
    try:
        with driver.session() as s:
            return [dict(r) for r in s.run(cypher, **params)], None
    except Exception as exc:
        return [], str(exc)


def _collect_fqns(rows: list[dict]) -> list[str]:
    fqns = []
    seen: set[str] = set()
    for row in rows:
        for v in row.values():
            if isinstance(v, str) and "." in v and v not in seen and not v.endswith(".java"):
                fqns.append(v)
                seen.add(v)
    return fqns


def _collect_file_paths(rows: list[dict]) -> list[str]:
    paths = []
    seen: set[str] = set()
    for row in rows:
        for v in row.values():
            if isinstance(v, str) and v.endswith(".java") and v not in seen:
                paths.append(v)
                seen.add(v)
    return paths


def _read_source_file(container_path: str) -> str | None:
    host_home = cfg.paths.host_home
    path = container_path
    if path.startswith("/host-home") and host_home:
        path = host_home + path[len("/host-home"):]
    try:
        content = Path(path).read_text(encoding="utf-8", errors="replace")
        if len(content) > _MAX_SOURCE_BYTES:
            content = content[:_MAX_SOURCE_BYTES] + f"\n\n... [file truncated at {_MAX_SOURCE_BYTES} chars]"
        return content
    except OSError:
        return None


def _kg(driver: Driver, cypher: str, **params) -> list[dict]:
    try:
        with driver.session() as s:
            return [dict(r) for r in s.run(cypher, **params)]
    except Exception as e:
        log.warning("KG query failed in nl_query", exc=e)
        return []


def _get_repo_path(driver: Driver, repo_id: str | None) -> str | None:
    if not repo_id:
        rows = _kg(driver, "MATCH (r:Repository) RETURN r.path AS path LIMIT 1")
    else:
        rows = _kg(driver, "MATCH (r:Repository {repo_id: $rid}) RETURN r.path AS path", rid=repo_id)
    if rows and rows[0].get("path"):
        return rows[0]["path"]
    return None


def _parse_unified_diff(raw: str) -> list[dict]:
    """
    Parse a unified diff string into a list of hunks, each with lines annotated
    as 'context', 'added', or 'removed'.
    """
    hunks = []
    current_hunk = None
    for line in raw.splitlines():
        if line.startswith("@@"):
            if current_hunk:
                hunks.append(current_hunk)
            current_hunk = {"header": line, "lines": []}
        elif current_hunk is not None:
            if line.startswith("+") and not line.startswith("+++"):
                current_hunk["lines"].append({"type": "added", "text": line[1:]})
            elif line.startswith("-") and not line.startswith("---"):
                current_hunk["lines"].append({"type": "removed", "text": line[1:]})
            else:
                current_hunk["lines"].append({"type": "context", "text": line[1:] if line.startswith(" ") else line})
    if current_hunk:
        hunks.append(current_hunk)
    return hunks


def _get_git_diff(repo_path: str, file_path: str) -> list[dict] | None:
    """
    Return parsed unified diff hunks for the last commit that touched file_path.
    file_path is the absolute container/host path; we derive the repo-relative path.
    """
    host_home = cfg.paths.host_home
    abs_path = file_path
    if abs_path.startswith("/host-home") and host_home:
        abs_path = host_home + abs_path[len("/host-home"):]

    # Make file path relative to the repo root
    try:
        repo_abs = str(Path(repo_path).resolve())
        file_abs = str(Path(abs_path).resolve())
        if not file_abs.startswith(repo_abs):
            return None
        rel_path = file_abs[len(repo_abs):].lstrip("/")
    except (OSError, ValueError):
        return None

    try:
        repo = gitlib.Repo(repo_path)
        # Find the last commit that touched this file
        commits = list(repo.iter_commits(paths=rel_path, max_count=1))
        if not commits:
            return None
        commit = commits[0]
        # Get the diff of that commit for just this file
        if commit.parents:
            diffs = commit.parents[0].diff(commit, paths=[rel_path], create_patch=True)
        else:
            diffs = commit.diff(gitlib.NULL_TREE, paths=[rel_path], create_patch=True)
        for d in diffs:
            raw = d.diff.decode("utf-8", errors="replace") if isinstance(d.diff, bytes) else d.diff
            hunks = _parse_unified_diff(raw)
            if hunks:
                return {
                    "hunks": hunks,
                    "commit_sha": commit.hexsha[:12],
                    "commit_message": commit.message.strip().splitlines()[0],
                    "commit_author": str(commit.author),
                    "commit_date": commit.committed_datetime.strftime("%Y-%m-%d %H:%M"),
                }
        return None
    except Exception:  # gitpython raises various exc types (InvalidGitRepositoryError, etc.)
        return None


def _is_code_reasoning(question: str) -> bool:
    keywords = [
        "bug", "fix", "crash", "exception", "error", "stack trace", "stacktrace",
        "why", "how does", "explain", "impact", "risk", "cause", "change",
        "ClassCastException", "npe", "nullpointer", "implement",
    ]
    q = question.lower()
    return any(kw.lower() in q for kw in keywords)


