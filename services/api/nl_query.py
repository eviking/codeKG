def _gather_impact(driver: Driver, fqns: list[str], repo_id: str | None) -> list[dict]:
    """
    Build an impact heatmap: for each affected class, find packages that
    depend on it via IMPORTS, EXTENDS, or IMPLEMENTS, grouped by package.
    """
    if not fqns:
        return []

    scope = f"AND dep.repo_id = '{repo_id}'" if repo_id else ""
    fqn_list = json.dumps(fqns)

    cypher = f"""
    UNWIND {fqn_list} AS target_fqn
    MATCH (target:Class {{fqn: target_fqn}})
    MATCH (dep:Class)-[r:IMPORTS|EXTENDS|IMPLEMENTS]->(target)
    WHERE dep.fqn <> target_fqn {scope}
    RETURN dep.fqn AS dependent_fqn,
           dep.file_path AS file_path,
           target_fqn AS affects,
           type(r) AS relation
    LIMIT 200
    """
    rows, _ = _run_cypher(driver, cypher)

    # Group by package
    by_pkg: dict[str, dict] = defaultdict(lambda: {"classes": set(), "relations": set(), "files": set()})
    for row in rows:
        dep_fqn = row.get("dependent_fqn", "")
        pkg = dep_fqn.rsplit(".", 1)[0] if "." in dep_fqn else dep_fqn
        by_pkg[pkg]["classes"].add(dep_fqn)
        by_pkg[pkg]["relations"].add(row.get("relation", ""))
        fp = row.get("file_path", "")
        if fp:
            by_pkg[pkg]["files"].add(fp)

    result = []
    for pkg, data in sorted(by_pkg.items(), key=lambda x: -len(x[1]["classes"])):
        result.append({
            "package": pkg,
            "class_count": len(data["classes"]),
            "relations": sorted(data["relations"]),
            "sample_classes": sorted(data["classes"])[:5],
        })

    return result[:20]


def _gather_test_scope(driver: Driver, fqns: list[str], repo_id: str | None) -> list[dict]:
    """
    Find test classes that are likely to cover the affected code,
    via IMPORTS edges and package proximity.
    """
    if not fqns:
        return []

    scope = f"AND t.repo_id = '{repo_id}'" if repo_id else ""
    fqn_list = json.dumps(fqns)

    # Direct: test classes that import an affected class
    direct_cypher = f"""
    UNWIND {fqn_list} AS target_fqn
    MATCH (t:Class)-[:IMPORTS]->(c:Class {{fqn: target_fqn}})
    WHERE (t.fqn CONTAINS 'Test' OR t.fqn CONTAINS 'Spec' OR t.fqn CONTAINS 'IT') {scope}
    RETURN DISTINCT t.fqn AS fqn, t.file_path AS file_path, 'direct_import' AS coverage_type
    LIMIT 20
    """
    direct_rows, _ = _run_cypher(driver, direct_cypher)

    # Proximity: test classes in the same package as affected classes
    packages = list({f.rsplit(".", 1)[0] for f in fqns if "." in f})
    if packages:
        pkg_list = json.dumps(packages)
        proximity_cypher = f"""
        UNWIND {pkg_list} AS pkg
        MATCH (t:Class)
        WHERE t.fqn STARTS WITH pkg
          AND (t.fqn CONTAINS 'Test' OR t.fqn CONTAINS 'Spec') {scope}
        RETURN DISTINCT t.fqn AS fqn, t.file_path AS file_path, 'same_package' AS coverage_type
        LIMIT 20
        """
        proximity_rows, _ = _run_cypher(driver, proximity_cypher)
    else:
        proximity_rows = []

    seen: set[str] = set()
    tests = []
    for row in direct_rows + proximity_rows:
        fqn = row.get("fqn", "")
        if fqn and fqn not in seen:
            seen.add(fqn)
            tests.append(row)

    return tests[:30]


def _gather_risk(driver: Driver, fqns: list[str], repo_id: str | None) -> dict:
    """
    Compute risk signals for the affected classes:
    - How many other classes depend on them (blast radius)
    - Whether they are generated code
    - How many sibling variants exist
    - Inheritance depth
    - Whether dedicated tests exist
    """
    if not fqns:
        return {}

    scope = f"AND dep.repo_id = '{repo_id}'" if repo_id else ""
    fqn_list = json.dumps(fqns)

    # Total dependants
    dep_cypher = f"""
    UNWIND {fqn_list} AS target_fqn
    MATCH (dep:Class)-[:IMPORTS|EXTENDS|IMPLEMENTS]->(c:Class {{fqn: target_fqn}})
    WHERE dep.fqn <> target_fqn {scope}
    RETURN count(DISTINCT dep) AS dependants
    """
    dep_rows, _ = _run_cypher(driver, dep_cypher)
    dependants = dep_rows[0].get("dependants", 0) if dep_rows else 0

    # Method count (complexity signal)
    method_cypher = f"""
    UNWIND {fqn_list} AS target_fqn
    MATCH (c:Class {{fqn: target_fqn}})-[:HAS_METHOD]->(m:Method)
    RETURN count(DISTINCT m) AS method_count
    """
    method_rows, _ = _run_cypher(driver, method_cypher)
    method_count = method_rows[0].get("method_count", 0) if method_rows else 0

    # Is generated?
    is_generated = any("/generated/" in fqn or "generated" in fqn.lower() for fqn in fqns)

    # Sibling variants — classes in the same package with similar naming pattern
    sibling_count = 0
    if fqns:
        pkg = fqns[0].rsplit(".", 1)[0] if "." in fqns[0] else ""
        base = re.sub(r'(DocValues|Source|Constant|Geo|Cartesian|AndSource|AndDocValues|AndConstant)', '', fqns[0].rsplit(".", 1)[-1])
        if pkg and base:
            sib_cypher = f"""
            MATCH (c:Class)
            WHERE c.fqn STARTS WITH '{pkg}.'
              AND toLower(c.fqn) CONTAINS toLower('{base[:15]}')
            RETURN count(c) AS siblings
            """
            sib_rows, _ = _run_cypher(driver, sib_cypher)
            sibling_count = max(0, (sib_rows[0].get("siblings", 1) if sib_rows else 1) - 1)

    # Test coverage
    test_cypher = f"""
    UNWIND {fqn_list} AS target_fqn
    MATCH (t:Class)-[:IMPORTS]->(c:Class {{fqn: target_fqn}})
    WHERE t.fqn CONTAINS 'Test' OR t.fqn CONTAINS 'Spec'
    RETURN count(DISTINCT t) AS test_count
    """
    test_rows, _ = _run_cypher(driver, test_cypher)
    test_count = test_rows[0].get("test_count", 0) if test_rows else 0

    if test_count == 0:
        coverage = "none"
    elif test_count < 3:
        coverage = "partial"
    else:
        coverage = "good"

    return {
        "dependants": dependants,
        "method_count": method_count,
        "is_generated": is_generated,
        "sibling_variants": sibling_count,
        "test_count": test_count,
        "test_coverage": coverage,
    }


def _gather_enriched(driver: Driver, fqns: list[str]) -> list[dict]:
    """
    Pull pre-computed enrichment signals (role, coupling, blast_size, summary,
    call_chains) for the matched classes. Used to give Claude richer context
    without extra source-file reads.
    """
    if not fqns:
        return []
    rows, _ = _run_cypher(driver, """
        UNWIND $fqns AS fqn
        MATCH (c:Class {fqn: fqn})
        RETURN c.fqn AS fqn, c.name AS name, c.role AS role,
               c.coupling AS coupling, c.blast_size AS blast_size,
               c.javadoc AS javadoc, c.summary AS summary,
               c.object_model AS object_model
    """, fqns=fqns[:20])

    result = []
    for r in rows:
        entry = {
            "fqn":      r.get("fqn"),
            "name":     r.get("name"),
            "role":     r.get("role"),
            "coupling": r.get("coupling"),
            "blast":    r.get("blast_size"),
            "javadoc":  r.get("javadoc"),
            "summary":  r.get("summary"),
        }
        # pull call_chains out of object_model if present
        om_str = r.get("object_model")
        if om_str:
            try:
                om = json.loads(om_str)
                chains = om.get("call_chains") or []
                if chains:
                    entry["call_chains"] = [
                        " → ".join(f"{s['name']}({s['role']})" for s in ch.get("chain", []))
                        for ch in chains[:2]
                    ]
                entry["warnings"] = om.get("warnings") or []
            except (ValueError, KeyError):
                pass
        result.append(entry)
    return result


def _enrich_with_siblings(driver: Driver, rows: list[dict], repo_id: str | None) -> list[dict]:
    seen_fps = {r.get("file_path") for r in rows}
    for row in list(rows):
        fqn = row.get("fqn") or ""
        if not fqn:
            continue
        pkg = fqn.rsplit(".", 1)[0] if "." in fqn else ""
        if not pkg:
            continue
        scope = f"AND c.repo_id = '{repo_id}'" if repo_id else ""
        cypher = (
            f"MATCH (c:Class) WHERE c.fqn STARTS WITH '{pkg}.' {scope} "
            f"RETURN c.fqn AS fqn, c.file_path AS file_path LIMIT 20"
        )
        sibling_rows, _ = _run_cypher(driver, cypher)
        for sr in sibling_rows:
            fp = sr.get("file_path", "")
            if fp and fp not in seen_fps:
                seen_fps.add(fp)
                rows.append(sr)
        break
    return rows


