# Main entry point
# ---------------------------------------------------------------------------

def ask(question: str, driver: Driver, repo_id: str | None = None) -> dict[str, Any]:
    scoped_question = question
    if repo_id:
        scoped_question = f"{question}\n\n(Scope to repo_id='{repo_id}' where relevant)"

    # Step 1: Generate primary Cypher
    cypher = ""
    fallback_cypher = ""
    rows = []
    cypher_error = ""

    try:
        with log_call("nl_query/cypher", cfg.llm.nl_query_model, scoped_question) as entry:
            resp = _llm.chat(
                model=cfg.llm.nl_query_model,
                prompt=scoped_question,
                system=_CYPHER_SYSTEM,
                max_tokens=cfg.llm.nl_cypher_max_tokens,
            )
            entry.record(resp)
            cypher = _extract_cypher(resp.text)
    except Exception as exc:
        return _error(question, f"Failed to generate Cypher: {exc}")

    rows, cypher_error = _run_cypher(driver, cypher)

    # Step 2: Fallback — only if primary returned nothing (lazy, no wasted LLM call)
    if cypher_error or not rows:
        try:
            with log_call("nl_query/cypher_fallback", cfg.llm.nl_query_model, scoped_question) as entry:
                resp = _llm.chat(
                    model=cfg.llm.nl_query_model,
                    prompt=scoped_question,
                    system=_FALLBACK_CYPHER_SYSTEM,
                    max_tokens=cfg.llm.nl_cypher_max_tokens // 2,
                )
                entry.record(resp)
                fallback_cypher = _extract_cypher(resp.text)
        except Exception:  # LLM call can fail many ways — fallback_cypher stays None
            pass
        if fallback_cypher:
            rows, cypher_error = _run_cypher(driver, fallback_cypher)

    used_cypher = fallback_cypher if (fallback_cypher and rows) else cypher
    code_question = _is_code_reasoning(question)

    # Step 3: Enrich with package siblings for code questions
    if code_question and rows:
        rows = _enrich_with_siblings(driver, rows, repo_id)

    # Step 4: Gather structured analysis from KG — all in parallel
    fqns = _collect_fqns(rows)
    repo_path = _get_repo_path(driver, repo_id)
    # Fetch repo language for language-aware prompts
    _lang_rows = _kg(driver, "MATCH (r:Repository {repo_id: $rid}) RETURN r.language AS language", rid=repo_id) if repo_id else []
    repo_language = (_lang_rows[0].get("language") or "Java") if _lang_rows else "Java"
    file_paths = _collect_file_paths(rows)[:_MAX_SOURCE_FILES] if code_question else []

    def _read_source_with_diff(fp: str) -> dict | None:
        content = _read_source_file(fp)
        if not content:
            return None
        result: dict = {"path": fp, "content": content}
        if repo_path:
            result["diff"] = _get_git_diff(repo_path, fp)
        return result

    with concurrent.futures.ThreadPoolExecutor(max_workers=6) as ex:
        f_impact   = ex.submit(_gather_impact,      driver, fqns, repo_id)
        f_tests    = ex.submit(_gather_test_scope,   driver, fqns, repo_id)
        f_risk     = ex.submit(_gather_risk,         driver, fqns, repo_id)
        f_enriched = ex.submit(_gather_enriched,     driver, fqns)
        f_sources  = [ex.submit(_read_source_with_diff, fp) for fp in file_paths]

        impact   = f_impact.result()
        tests    = f_tests.result()
        risk     = f_risk.result()
        enriched = f_enriched.result()

    # Step 5: Collect source file results
    sources: list[dict] = [r for f in f_sources if (r := f.result()) is not None]

    # Step 6: Build answer with full context
    try:
        # Build enriched class lines using pre-computed signals where available
        class_lines = []
        enriched_by_fqn = {e["fqn"]: e for e in enriched}
        for f in fqns[:20]:
            e = enriched_by_fqn.get(f)
            if e:
                line = f"  - {f}"
                parts = []
                if e.get("role"):    parts.append(f"role={e['role']}")
                if e.get("coupling") is not None: parts.append(f"coupling={e['coupling']:.2f}")
                if e.get("blast"):   parts.append(f"blast_radius={e['blast']}")
                if parts: line += f"  [{', '.join(parts)}]"
                if e.get("javadoc"): line += f"\n      Javadoc: {e['javadoc'][:200]}"
                if e.get("summary"): line += f"\n      Summary: {e['summary'][:200]}"
                if e.get("call_chains"):
                    for ch in e["call_chains"]:
                        line += f"\n      Call chain: {ch}"
                if e.get("warnings"):
                    line += f"\n      Warnings: {'; '.join(e['warnings'][:2])}"
                class_lines.append(line)
            else:
                class_lines.append(f"  - {f}")

        kg_summary = (
            f"Affected classes ({len(fqns)}):\n" +
            "\n".join(class_lines)
        )
        impact_summary = ""
        if impact:
            impact_summary = f"\n\nImpact — {sum(p['class_count'] for p in impact)} dependent classes across {len(impact)} packages:\n"
            for p in impact[:5]:
                impact_summary += f"  - {p['package']} ({p['class_count']} classes, via {', '.join(p['relations'])})\n"

        risk_summary = ""
        if risk:
            risk_summary = (
                f"\n\nRisk signals:\n"
                f"  - Direct dependants: {risk.get('dependants', 0)}\n"
                f"  - Sibling variants with same pattern: {risk.get('sibling_variants', 0)}\n"
                f"  - Test coverage: {risk.get('test_coverage', 'unknown')} ({risk.get('test_count', 0)} test classes)\n"
                f"  - Generated code: {risk.get('is_generated', False)}\n"
            )

        test_summary = ""
        if tests:
            test_summary = f"\n\nTest classes to run ({len(tests)}):\n"
            for t in tests[:10]:
                test_summary += f"  - {t.get('fqn', '')} [{t.get('coverage_type', '')}]\n"

        source_context = ""
        if sources:
            source_context = "\n\nSource files:\n"
            for s in sources:
                source_context += f"\n--- {s['path']} ---\n{s['content']}\n"

        answer_prompt = (
            f"Question: {question}\n\n"
            f"{kg_summary}{impact_summary}{risk_summary}{test_summary}"
            f"{source_context}"
        )

        # Use the answer model for complex reasoning, query model for simple factual questions
        answer_model = (
            cfg.llm.nl_answer_model if (code_question or len(fqns) > 10 or sources)
            else cfg.llm.nl_query_model
        )
        answer_max_tokens = (
            cfg.llm.nl_answer_max_tokens if answer_model == cfg.llm.nl_answer_model
            else cfg.llm.nl_answer_max_tokens_fast
        )

        with log_call("nl_query/answer", answer_model, answer_prompt) as entry:
            answer_msg = _llm.chat(
                model=answer_model,
                prompt=answer_prompt,
                system=_answer_system(repo_language),
                max_tokens=answer_max_tokens,
            )
            entry.record(answer_msg)
        summary = answer_msg.text.strip()
        tokens_with_kg = {
            "input":  answer_msg.usage.input_tokens,
            "output": answer_msg.usage.output_tokens,
        }
    except Exception as exc:
        summary = f"Analysis failed: {exc}"
        tokens_with_kg = {}

    # Step 7: Compute KG efficiency metrics
    kg_efficiency = _gather_kg_efficiency(driver, fqns, sources, tokens_with_kg, repo_id)

    return {
        "question": question,
        "cypher": used_cypher,
        "summary": summary,
        "impact": impact,
        "test_scope": tests,
        "risk": risk,
        "sources": sources,
        "tokens_with_kg": tokens_with_kg,
        "kg_efficiency": kg_efficiency,
        "error": cypher_error if not rows else None,
    }


def _error(question: str, msg: str) -> dict[str, Any]:
    return {
        "question": question,
        "cypher": "",
        "summary": "",
        "impact": [],
        "test_scope": [],
        "risk": {},
        "sources": [],
        "tokens_with_kg": {},
        "kg_efficiency": {},
        "error": msg,
    }
