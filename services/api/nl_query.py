_CHARS_PER_TOKEN = 4  # rough approximation


def _gather_kg_efficiency(
    driver: Driver,
    fqns: list[str],
    sources: list[dict],
    tokens_with_kg: dict,
    repo_id: str | None,
) -> dict:
    """
    Estimate the token cost of answering the same question WITHOUT the KG,
    by simulating what an LLM agent would actually have to do:

      1. Scan all files in the affected packages to find relevant classes
      2. Read those files in full to understand types, relationships, callers
      3. Repeat for each dependency layer

    All inputs come from the KG itself — we use it to measure what would be
    needed without it.
    """
    if not fqns:
        return {}

    host_home = cfg.paths.host_home
    scope = f"AND c.repo_id = '{repo_id}'" if repo_id else ""

    # ── 1. Files the agent would have to scan ──────────────────────────────
    # All files in the same packages as the affected classes
    packages = list({f.rsplit(".", 1)[0] for f in fqns if "." in f})
    pkg_list = json.dumps(packages)

    scan_cypher = f"""
    UNWIND {pkg_list} AS pkg
    MATCH (c:Class)
    WHERE c.fqn STARTS WITH pkg {scope}
    RETURN DISTINCT c.file_path AS file_path
    """
    scan_rows, _ = _run_cypher(driver, scan_cypher)
    files_to_scan = [r["file_path"] for r in scan_rows if r.get("file_path")]

    # Also include files of direct dependants (one hop) — agent needs those
    # to understand callers
    dep_cypher = f"""
    UNWIND {json.dumps(fqns)} AS target_fqn
    MATCH (dep:Class)-[:IMPORTS|EXTENDS|IMPLEMENTS]->(c:Class {{fqn: target_fqn}})
    WHERE dep.fqn <> target_fqn {scope}
    RETURN DISTINCT dep.file_path AS file_path
    LIMIT 50
    """
    dep_rows, _ = _run_cypher(driver, dep_cypher)
    dep_files = [r["file_path"] for r in dep_rows if r.get("file_path")]

    all_files_to_scan = list(dict.fromkeys(files_to_scan + dep_files))  # preserve order, dedupe

    # ── 2. Measure actual file sizes ───────────────────────────────────────
    total_scan_chars = 0
    files_readable = 0
    for fp in all_files_to_scan:
        path = fp
        if path.startswith("/host-home") and host_home:
            path = host_home + path[len("/host-home"):]
        try:
            size = Path(path).stat().st_size
            total_scan_chars += size
            files_readable += 1
        except OSError:
            total_scan_chars += 3000  # estimate for unreadable files

    estimated_scan_tokens = total_scan_chars // _CHARS_PER_TOKEN

    # ── 3. Round trips estimate ────────────────────────────────────────────
    # Typical agent pattern: 1 search call + 1 read per file in the package,
    # then 1 more search + read per dependency layer
    n_packages = len(packages)
    n_dep_files = len(dep_files)
    estimated_round_trips = n_packages + len(files_to_scan) + max(1, n_dep_files // 5)

    # ── 4. KG context token cost (what we actually sent) ──────────────────
    kg_input_tokens = tokens_with_kg.get("input", 0)
    # Tokens from just the source files we read (subset of all scanned)
    source_chars = sum(len(s["content"]) for s in sources)
    source_tokens = source_chars // _CHARS_PER_TOKEN

    # KG structural context tokens (everything except source files)
    kg_structural_tokens = max(0, kg_input_tokens - source_tokens)

    return {
        "files_kg_read":          len(sources),
        "files_agent_would_scan": len(all_files_to_scan),
        "files_in_packages":      len(files_to_scan),
        "files_dependants":       len(dep_files),
        "estimated_scan_tokens":  estimated_scan_tokens,
        "estimated_round_trips":  estimated_round_trips,
        "kg_input_tokens":        kg_input_tokens,
        "kg_structural_tokens":   kg_structural_tokens,
        "source_tokens":          source_tokens,
        "token_ratio":            round(estimated_scan_tokens / kg_input_tokens, 1) if kg_input_tokens else 0,
    }


